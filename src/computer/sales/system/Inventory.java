    private void u_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_u_searchKeyReleased

        //user panel search button.
        String key = u_search.getText();    //search box eke type wena eka key kyna variable ekat gnnw 
        userRecode = (DefaultTableModel) userTable.getModel();      //table model ekk hdala ekata db eke table eka load krgnnw

        rowCount = userTable.getRowCount();     //table eke row count eka gnnwa meka ona wenne search weddi row ain krnn table ekan

        while (rowCount-- > 0) {    //meken row gana ain kra kra search krna row eka vtrak ithuru krnwa
            userRecode.removeRow(rowCount);
        }

        try {

            DataOb.dbConnects();    //dataob ekn db connection eka dnnwa 
            System.out.println("Database connection success...");
            res = DataOb.viewLike(dbusers, "UName", key); //data ob eke viewlike kyna methord eka *quary eka thyenne methana* call krnawa
            //ekata database eke search rna table name ekai, coulumn name ekai, uda type wena ke word eka inpun wena variable ekai denwa *key eka*

            while (res.next()) {    //search una data row tika aye jtable ekat add krnwa
                Vector columnData = new Vector();

                columnData.add(res.getString("User_ID"));
                columnData.add(res.getString("UName"));
                columnData.add(res.getString("UNIC"));
                columnData.add(res.getString("UAddress"));
                columnData.add(res.getString("UPhone"));
                columnData.add(res.getString("UEmail"));
                columnData.add(res.getString("Salary"));
                columnData.add(res.getString("Joined_date"));
                columnData.add(res.getString("Post"));

                userRecode.addRow(columnData); //aluth column ekk hdana eka krnna awashya unoth 
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);    //error ekk awoth eka show krnwa
        }

    }//GEN-LAST:event_u_searchKeyReleased

