    private void select_SearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_select_SearchKeyReleased
        
        //item panel search button.
        String key = select_Search.getText();
        selectRecode = (DefaultTableModel) selectItemTable.getModel();

        rowCount = selectItemTable.getRowCount();

        while (rowCount-- > 0) {
            selectRecode.removeRow(rowCount);
        }

        try {

            DataOb.dbConnects();
            System.out.println("Database connection success...");
            res = DataOb.viewLike("jItemTableView", "IName", key);

            while (res.next()) {
                Vector columnData = new Vector();

                columnData.add(res.getString("Item_ID"));
                columnData.add(res.getString("IName"));
                columnData.add(res.getString("Quantity"));
                columnData.add(res.getString("IsellingPrice"));

                selectRecode.addRow(columnData);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_select_SearchKeyReleased

