    public void upDateItemTable() {

        String[] arrProperty = {"a.Item_ID", "a.IName", "b.Quantity", "a.IsellingPrice"};

        try {
            DataOb.dbConnects();
            System.out.println("Database connection success...");

            res = DataOb.selectedField("items a, stock b", arrProperty, "a.Item_ID", "b.Item_ID", "");
            ResultSetMetaData stData = res.getMetaData();

            qcount = stData.getColumnCount();

            DefaultTableModel userRecord = (DefaultTableModel) selectItemTable.getModel();
            userRecord.setRowCount(0);

            while (res.next()) {
                Vector columnData = new Vector();

                for (i = 1; i <= qcount; i++) {
                    columnData.add(res.getString("Item_ID"));
                    columnData.add(res.getString("IName"));
                    columnData.add(res.getString("Quantity"));
                    columnData.add(res.getString("IsellingPrice"));

                }
                userRecord.addRow(columnData);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    
